using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BasicComputation
{

    // Paala, James P.          BSIT - 501
    internal class BasicComputation
    {

        public float Addition(float x, float y)
        {
            return x + y;
        }
        public float Subraction(float x, float y)
        {
            return x - y;
        }
        public float Multiplication(float x, float y)
        {
            return x * y;
        }
        public float Division(float x, float y)
        {
            return x / y;
        }
    }
}
